//! Hardcoded GID-to-Unicode mapping tables for common non-embedded fonts.
//!
//! When a CIDFontType2 font with Identity-H encoding is not embedded in the PDF,
//! the CID values in text strings are glyph indices (GIDs) from the original font.
//! A substitute font has different glyph ordering, so CID-as-GID produces garbled
//! text. These tables map known fonts' GIDs to Unicode code points, enabling
//! correct rendering with any substitute font.
//!
//! Tables derived from Mozilla PDF.js (Apache-2.0 licensed):
//! - `getGlyphMapForStandardFonts()` — base table for common TrueType fonts
//! - `getSupplementalGlyphMapForCalibri()` — Calibri-specific overrides

use std::collections::HashMap;

/// Build a GID-to-Unicode map for a known font family.
///
/// Returns `Some(map)` if the font family has a known mapping, `None` otherwise.
/// The font family name should be lowercase, stripped of subset prefix and style
/// suffix (e.g. "calibri" not "ABCDEF+Calibri,Bold").
pub fn get_gid_to_unicode_map(font_family: &str) -> Option<HashMap<u16, u32>> {
    match font_family {
        "calibri" => {
            let mut map = standard_glyph_map();
            // Calibri overrides: these GIDs differ from the generic table
            for (gid, unicode) in CALIBRI_SUPPLEMENT {
                map.insert(*gid, *unicode);
            }
            Some(map)
        }
        // These fonts share the standard TrueType glyph ordering
        "tahoma" | "verdana" | "arial" | "trebuchet ms" | "trebuchetms" | "centurygothic"
        | "century gothic" | "comicsansms" | "comic sans ms" | "consolas" | "georgia"
        | "impact" | "lucidaconsole" | "lucida console" | "palatinolinotype"
        | "palatino linotype" | "segoeui" | "segoe ui" | "timesnewroman" | "times new roman"
        | "couriernew" | "courier new" | "cambria" | "candara" | "constantia" | "corbel" => {
            Some(standard_glyph_map())
        }
        // ArialBlack uses the standard map plus overrides for Polish chars
        "arialblack" | "arial black" => {
            let mut map = standard_glyph_map();
            for (gid, unicode) in ARIAL_BLACK_SUPPLEMENT {
                map.insert(*gid, *unicode);
            }
            Some(map)
        }
        // Fallback: for any non-embedded CIDFontType2 + Identity-H font,
        // the standard TrueType glyph ordering is the best guess. Without
        // any mapping the text is guaranteed garbled; the standard map is
        // correct for the vast majority of Western TrueType fonts.
        _ => Some(standard_glyph_map()),
    }
}

/// Base GID-to-Unicode table for standard TrueType fonts.
///
/// Covers basic Latin, extended Latin, Cyrillic, Hebrew, Arabic diacriticals,
/// and assorted symbols. 496 entries.
fn standard_glyph_map() -> HashMap<u16, u32> {
    let mut m = HashMap::with_capacity(STANDARD_GLYPHS.len());
    for (gid, unicode) in STANDARD_GLYPHS {
        m.insert(*gid, *unicode);
    }
    m
}

